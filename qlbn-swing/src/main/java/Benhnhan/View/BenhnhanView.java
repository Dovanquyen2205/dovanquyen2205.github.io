    public int countPatientsDischargedThisMonth(List<Benhnhan> patients) {
        int count = 0;
        Date today = new Date(); // Lấy ngày hiện tại
        Calendar calendar = Calendar.getInstance();
        calendar.setTime(today);
        int currentMonth = calendar.get(Calendar.MONTH);
        int currentYear = calendar.get(Calendar.YEAR);
        
        for (Benhnhan patient : patients) {
            if(patient.getNgayXuatVien() != null){
                calendar.setTime(patient.getNgayXuatVien());
                int patientMonth = calendar.get(Calendar.MONTH);
                int patientYear = calendar.get(Calendar.YEAR);
                if (patientMonth == currentMonth && currentYear == patientYear) {
                    count++;
                }
            }
            
        }
        
        return count;
    }
    
