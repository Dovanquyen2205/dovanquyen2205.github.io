    private void chonanhActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chonanhActionPerformed
        // TODO add your handling code here:
        JFileChooser chooser = new JFileChooser();
        chooser.showOpenDialog(null);
        File file = chooser.getSelectedFile();
        if (file != null) {
            try {
                // Đọc ảnh từ tệp đã chọn
                Image image = ImageIO.read(file);
                // Thay đổi kích thước của ảnh để lấp đầy khung JLabel
                Image scaledImage = image.getScaledInstance(anh.getWidth(), anh.getHeight(), Image.SCALE_SMOOTH);
                // Tạo một ImageIcon từ ảnh đã thay đổi kích thước
                ImageIcon icon = new ImageIcon(scaledImage);
                // Đặt icon vào JLabel để hiển thị ảnh
                anh.setIcon(icon);
            } catch (IOException ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_chonanhActionPerformed

