    public void showtablebenhnhan(List<Benhnhan> listbenhnhan) {
        //hien thi trang chu
        int tongbenhnhan = 0;
        int tongbenhan = 0;
        int tongbenhnhanravien = 0;
        int tongbenhnhandangdieutri = 0;
        DefaultTableModel model1 = (DefaultTableModel) this.BNTable.getModel();
        model1.setNumRows(0);
        DefaultTableModel model2 = (DefaultTableModel) this.benhantable.getModel();
        model2.setNumRows(0);
        int n1 = 1;
        int n2 = 1;
        for (Benhnhan x : listbenhnhan) {
            if (x.getTinhTrangXuatVien().equalsIgnoreCase("Đã xuất viện")) {
                tongbenhnhanravien++;
            } else if (x.getTinhTrangXuatVien().equalsIgnoreCase("Đang điều trị")) {
                tongbenhnhandangdieutri++;
            }
            tongbenhnhan++;
            tongbenhan++;
            model1.addRow(new Object[]{n1++, x.getHoTen(), dateToString(x.getNgaySinh()), x.getGioiTinh(),
                x.getQueQuan(), x.getHoKhauThuongTru(),x.getMaBenhNhan(), x.getMaBHYT(), x.getSoDienThoai(), x.getTinhTrangXuatVien()});
            model2.addRow(new Object[]{n2++, x.getHoTen(), dateToString(x.getNgaySinh()),
                    x.getGioiTinh(), x.getMaBenhNhan(), x.getTrieuChung(), x.getChanDoan(),
                    x.getChiDinh(), x.getBacSiKham(), x.getKetLuan(), dateToString(x.getNgayNhapVien()), dateToString(x.getNgayXuatVien())});
        }

        tongsobenhnhan.setText(String.valueOf(tongbenhnhan));
        tongsoxuatvien.setText(String.valueOf(tongbenhnhanravien));
        tongsodangdieutri.setText(String.valueOf(tongbenhnhandangdieutri));
    }

    public void showtablethuoc(List<Thuoc> listthuoc) {
        int tongdonthuoc = 0;
        DefaultTableModel model = (DefaultTableModel) this.thuoctable.getModel();
        model.setNumRows(0);
        int n = 1;
        for (Thuoc x : listthuoc) {
            tongdonthuoc++;
            model.addRow(new Object[]{n++, x.getMaThuoc(), x.getTenThuoc(),
                x.getCongDung(), dateToString(x.getNgaySanXuat()), dateToString(x.getHanSuDung()),
                x.getDonViTinh(), x.getSoLuong(), x.getGiaBan(), x.getSoLuong()*x.getGiaBan(), x.getMaBenhNhan()});
        }
        tongsothuoc.setText(String.valueOf(tongdonthuoc));
    }

