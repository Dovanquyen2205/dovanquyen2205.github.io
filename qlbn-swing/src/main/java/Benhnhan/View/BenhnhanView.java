    private static Image byteArrayToImage(byte[] bytes) {
        try {
            BufferedImage image;
            try (ByteArrayInputStream bis = new ByteArrayInputStream(bytes)) {
                image = ImageIO.read(bis);
            }
            return image;
        } catch (IOException e) {
            e.printStackTrace();
            return null;
        }
    }
    private static byte[] labelToByteArray(JLabel label) {
        // Lấy biểu tượng từ JLabel
        Icon icon = label.getIcon();
        if (icon == null || !(icon instanceof ImageIcon)) {
            System.err.println("No image found in the label!");
            return null;
        }

        // Chuyển đổi biểu tượng thành hình ảnh BufferedImage
        Image image = ((ImageIcon) icon).getImage();
        BufferedImage bufferedImage = new BufferedImage(image.getWidth(null), image.getHeight(null), BufferedImage.TYPE_INT_RGB);
        Graphics2D g2d = bufferedImage.createGraphics();
        g2d.drawImage(image, 0, 0, null);
        g2d.dispose();

        // Ghi hình ảnh từ BufferedImage vào một mảng byte
        ByteArrayOutputStream baos = new ByteArrayOutputStream();
        try {
            ImageIO.write(bufferedImage, "jpg", baos);
        } catch (IOException e) {
            e.printStackTrace();
        }
        return baos.toByteArray();
    }
    
